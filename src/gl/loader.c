int load_gl_procs()
{
    lx_glGetString = (PFNGLGETSTRINGPROC) load_proc("glGetString");
    if (!lx_glGetString)
    {
        lx_set_last_error("failed to load required gl functions for a version check");
        return 0;
    }
    
    int version = query_supported_gl_version(); 
    if (version == 0)
    {
        lx_set_last_error("failed to query supported opengl version");
        return 0;
    }
    
    load_1_0();
    load_1_1();
    load_1_2();
    load_1_3();
    load_1_4();
    load_1_5();
    load_2_0();
    load_2_1();
    load_3_0();
    load_3_1();
    load_3_2();
    load_3_3();
    load_4_0();
    load_4_1();
    load_4_2();
    load_4_3();
    load_4_4();
    load_4_5();
    load_4_6();

    return version;
}
