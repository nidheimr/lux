static int query_supported_gl_version()
{
    GLubyte* version = lx_glGetString(GL_VERSION);
    if (!version) return 0;
    
    int major = 0;
    int minor = 0;
    sscanf((const char*)version, "%d.%d", &major, &minor);

    v1_0 = (major == 1 && minor >= 0) || major > 1;
    v1_1 = (major == 1 && minor >= 1) || major > 1;
    v1_2 = (major == 1 && minor >= 2) || major > 1;
    v1_3 = (major == 1 && minor >= 3) || major > 1;
    v1_4 = (major == 1 && minor >= 4) || major > 1;
    v1_5 = (major == 1 && minor >= 5) || major > 1;

    v2_0 = (major == 2 && minor >= 0) || major > 2;
    v2_1 = (major == 2 && minor >= 1) || major > 2;

    v3_0 = (major == 3 && minor >= 0) || major > 3;
    v3_1 = (major == 3 && minor >= 1) || major > 3;
    v3_2 = (major == 3 && minor >= 2) || major > 3;
    v3_3 = (major == 3 && minor >= 3) || major > 3;

    v4_0 = (major == 4 && minor >= 0) || major > 4;
    v4_1 = (major == 4 && minor >= 1) || major > 4;
    v4_2 = (major == 4 && minor >= 2) || major > 4;
    v4_3 = (major == 4 && minor >= 3) || major > 4;
    v4_4 = (major == 4 && minor >= 4) || major > 4;
    v4_5 = (major == 4 && minor >= 5) || major > 4;
    v4_6 = (major == 4 && minor >= 6) || major > 4;
    
    return (major * 10000) + minor;
}

